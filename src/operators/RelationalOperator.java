package operators;

public class RelationalOperator {
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.println(a==b);       //10==20
        System.out.println(a!=b);    //10!=20
        System.out.println(a>b);    //10>20
        System.out.println(a<b);     //10<20
        System.out.println(a<=b);    //10<=20
        System.out.println(a>=b);      //10>=20
    }
}
